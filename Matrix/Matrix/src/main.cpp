void test2(Matrix *matrix, unsigned long timeDelay);
void test3(Matrix *matrix, unsigned long timeDelay);
void test4(Matrix *matrix, unsigned long timeDelay);


void setup(void) {
  matrix.init();
  matrix.clear();

  // Serial
  Serial.begin(57600);
  while (!Serial) {}
}

